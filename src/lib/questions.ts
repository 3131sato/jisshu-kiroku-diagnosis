// --- タイプ確定後の深掘り質問(各タイプ7問、Yes/Noどちらでも次に進む) ---
type DeepItem = {
  text: string;
  yes: string;
  no: string;
};

function buildDeepNodes(typeId: TypeId, items: DeepItem[]): QuestionNode[] {
  return items.map((item, i) => {
    const id = `dq-${typeId}-${i + 1}`;
    const nextId = i + 1 < items.length ? `dq-${typeId}-${i + 2}` : null;
    return {
      id,
      text: item.text,
      choices: [
        { label: "はい、当てはまる", explanation: item.yes, next: nextId },
        { label: "いいえ、あまりない", explanation: item.no, next: nextId },
      ],
    };
  });
}

